def parse(ul, item, level, file_index=1):

    # 获取当前 ul 下的直接 li 子元素
    lis = ul.xpath('./li')
    for li in lis:
        base_item = copy.deepcopy(item)
        a_list = li.xpath('./a')
        a = a_list[0]
        href = a.get('href').strip()
        title = a.xpath('string(.)').strip()
        base_item[f'title_level_{level}'] = title
        base_item[f'index_{level}'] = file_index
        base_item[f'filepath'] = href
        file_index += 1
        # 递归处理当前 ul 下的直接子 ul
        sub_uls = li.xpath('./ul')
        if not sub_uls:
            yield base_item
        else:
            for index, sub_ul in enumerate(sub_uls, 1):
                yield from parse(sub_ul, base_item, level+1, index)


